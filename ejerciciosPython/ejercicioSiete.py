# Calcular el monto a pagar en una cabina de Internet si el costo por hora es de 1200 pesos.



horas = int(input("por favor introduce el numero de horas: "))
costoPorHora = 1200
montoApagar = costoPorHora * horas;
print("El monto a pagar si usa la cabina de internet por " + str(horas) + " horas, es de $" + str(montoApagar))